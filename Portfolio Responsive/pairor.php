                <section class="project-detail">
                    <h3>GESTION</h3>
                    <p>J’ai travaillé en utilisant les méthodes agiles, particulièrement la méthode SCRUM. Ce qui m’a permis de travailler par incrémentation, en prenant en compte les retours des différents partis concernés pour chaque étape. L’avancement du projet s’est fait sans encombres, les différents collaborateurs ont été très fiers de participer à la refonte de l’expérience Pair’Or.</p>
                    </br>

                    <h3>LE DÉFI</h3>
                    <p>Les bateaux ayant beaucoup d’options et leurs prix étant élevés, un simple achat par ajout au panier n’est pas approprié. Les dirigeants de la société préfèrent  l’achat via la demande de devis, qui leur permet, lors d’un achat conséquent de bateaux par un club, de faire un geste commercial, procurant ainsi un service personnalisé à chaque client (clubs d’aviron). Le défi ici est de mettre en place un parcourt d’achat similaire à celui que l’on retrouve sur un site e-commerce classique, mais sans ajout au panier, mais avec plutôt une demande de devis. Et ce, sans sacrifier l’expérience utilisateur gloable du site.</p>
                    </br>

                    <h3>L’APPROCHE</h3>
                    <p>L’image que veux se donner Pair’Or, est une image similaire à Tesla mais pour le monde de l’aviron, celle d’une start-up disruptive dans son domaine. Je me suis inspiré des codes d’ergonomie du site Tesla, ainsi que les interfaces des sites des sociétés innovantes orienté B2B. Mais pas que, car grâce à l’étude faite auprès de l’échantillon d’utilisateurs du site Pair’Or, j’ai pu croiser toutes ces précieuses données, pour en retirer, via la création de personae, une expérience d’utilisation optimale pour chaque profil/cible. Avec le persona du club d’aviron (et du particulier dans une moindre mesure), j’ai pû mettre en place une interface utilisateur approprié.</p>
                    </br>

                    <h3>LA VISION</h3>
                    <p>Ma vision pour Pair’Or est de faire un site avec une interface simple, allant droit à l’information. En prenant en compte tous les éléments mentionnés plus haut, j’espère pouvoir placer Pair’Or, en terme d’interface web, de référence dans le milieu de l’aviron. Je ne voulais pas offrir un énième catalogue de produit, au contraire je voulais amener les utilisateurs à découvrir l’univers de la marque, son histoire et ses produits qu’ils adoreront. Les clients attendent de nous de la clarté pour un produit qui à un prix élevé et qui est hautement personalisable. C’est pour cela que j’ai imaginé le futur de l’achat de bateaux d’aviron via devis en ligne, avec des fiches caractéristiques détaillées et ordonnées, offrant une visibilité des options disponibles. Le tout sans noyer le client dans la sur-information.</p></br>
