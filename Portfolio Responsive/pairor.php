                    <h3>LE SERVICE</h3>
                    <p>Pair’Or propose 12 modeles bateaux d’aviron , allant de l’entraiement à la compétition. Ce sont des bateaux légés en carbone, offrant au rameur de meilleures sensations sur l’eau et un plus grand contrôle du bateau sur l’eau. Inspirés du monde automobile les bateaux sont personalisables et viennent avec plusieurs options. Il y a 3 gammes, la gamme OR, ARGENT et BRONZE, chacune étant faite pour un type d’utilisation spécial. À cela, le client peut rajouter plusieurs équipements et accessoires, afin d’avoir un produit qui convienne à ses besoins. À cause de cela, et aux coûts du produit, les bateaux ne peuvent qu’être achetés via un devis.</p>
                    </br>

                    <h3>LES TECHNOLOGIES</h3>
                    <p>Je me suis servi principalement de Sketch pour la refonte de la charte graphique et du site web. Pour le développement du site, j’ai utilisé plusieurs langages: HTML5, CSS3 Bootstrap, Jquery, Masonry.</p>
                    </br>

                    <h3>LA MISE EN LIGNE</h3>
                    <p>D’abord, le 12 Juin 2016, la nouvelle charte graphique remplace l’ancienne sur les réseaux sociaux. Le lancement du site lui se fera courant Juillet 2016.</p>
                    </br>

                    <h3>RECOMMENDATION FUTURE</h3>
                    <p>Pour la  page « Pièces Détachées », une solution e-commerce classique avec ajout au panier aurait convenue. Mais malgré mes propositions aux dirigeants, ceux-ci pour des problèmes logistiques, de gestion interne et de livraison, ne peuvent pas vendre sans devis les produits de cette page. Toutefois je leur ai recommendé, le jour où ils seront prêts à vendre ces produits sans devis, de me contacter pour que je ré-adapte le menu, avec un accès au panier, et la page en question avec un ajout au panier pour chaque article.</p>
                    </br>

