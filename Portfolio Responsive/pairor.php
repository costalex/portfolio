                    <p class="cartouche">Pair’Or est une jeune marque de bateaux d’aviron pour un sport centenaire. Fondée en 2015 à Bordeaux par deux champions. Michel Andrieux, Champion Olympique, médaille d’Or à Sydney en 2000 et Thierry Costa, Champion de France à plusieurs reprise.</p>
                    </br>

                    <h3>UN PEU PLUS SUR PAIR’OR</h3>
                    <p>Insatisfaits de l’offre sur le marché des bateaux et accessoires d’aviron, Michel et Thierry décidèrent de lancer leur propre marque, décomplexés et en restant professionnels. C’est une marque faite par des athlètes pour les athletes, mot d’ordre lors de la refonte de cette marque.</p>
                    </br>

                    <h3>MON RÔLE</h3>
                    <p>Je me suis occupé de la refonte de la charte graphique ainsi que du site web. Pour ce projet j’avais « carte blanche » de la part des propriétaires. En amont, et tout au long de mon travail j’ai échangé avec eux afin de connaitre leurs besoins. Mais aussi pour leur montrer les attentes des utilisateurs et surtout de leur proposer une expérience utilisateur en conformité avec leurs produits, services et utilisateurs.</p>
                    </br>

                    <h3>RETOURS UTILISATEURS & CONCEPTUALISATION</h3>
                    <p>J’ai pris contact avec 8 de leurs utilisateurs (du site web, non du produit), 2 rencontrés en personne et les 6 autres via le téléphone. Cela afin d’avoir un retour de leurs attentes lordqu’ils naviguent sur le site, de ce qu’ils cherchent et surtout de leur façon de naviguer et de s’appropier le site. Afin de traduire ces concepts en caractéristiques qui répondent aux comportements et aux motivations des ces internautes.</p>
                    </br>
            
                    <h3>VISION & STRATÉGIE D’EXPÉRIENCE</h3>
                    <p>J’ai réalisé des roughs et des wireframes afin de partager ma vision, ma stratégie de contenu et des recommendations de design avec les dirigeants de Pair’Or. Facilitant ainsi l’approbation et la compréhension des disigns, des idées et principes d’ergonomie. Une fois dans cet environnement de confiance, la prise de décisions des différents partis, fût grandement accélérée.</p>
                    </br>

                    <h3>PLANIFICATION & PÉRIMÈTRE D’INTERVENTION</h3>
                    <p>Nous avons définis ensemble le périmètre d’intervention sur lequel j’interviendrai. La refonte de la charte graphique, du site et d’un sotck d’images et contenu à poster sur Facebook lors de moments clés à venir. J’ai négocié, prioriesé et plannifié le lancement des différentes caractéristiques du site, les objectifs des utilisateurs et les objectifs mercantiles.</p>
                    </br>

                    <h3>SUPERVISION & COORDINATION</h3>
                    <p>J’ai collaboré avec les dirigeant de Pair’Or, leurs fournisseurs, leurs clients et des rameurs pour la refonte graphique du logo et du site web. Avec les retours de chacun, j’ai pu traduire leurs attentes en fonctionnalités et créer une expérience utilisateur optimale à leur environnement et à leur cible.</p>
                    </br>

                    <h3>EXÉCUTION DES DESIGNS & VALIDATIONS</h3>
                    <p>J’ai réalisé des parcours utilisateurs, des personae, des roughs, des wireframes et des versions responsives, validés auprès de Thierry Costa et Michel Andrieux.</p></br> 
